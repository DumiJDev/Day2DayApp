package ao.dumij.day2day.controllers;

public class CustomThymeleaf {
	
	private String classHTML;
	private String value;
	private Boolean elemBool;
	
	public String getClassHTML() {
		return classHTML;
	}
	
	public void setClassHTML(String classHTML) {
		this.classHTML = classHTML;
	}
	
	public String getValue() {
		return value;
	}
	
	public void setValue(String value) {
		this.value = value;
	}

	public Boolean getElemBool() {
		return elemBool;
	}

	public void setElemBool(Boolean elemBool) {
		this.elemBool = elemBool;
	}
	
	
}
